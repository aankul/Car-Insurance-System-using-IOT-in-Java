    public void populateTable()
    {
        DefaultTableModel model = (DefaultTableModel) WorkRequestjTable.getModel();
        model.setRowCount(0);
        for (WorkRequest workRequest : userAccount.getWorkQueue().getWorkRequestList()) {
                Object[] row = new Object[6];
                row[0] = workRequest;
                row[1] = workRequest.getRequestDate();
                row[2] = workRequest.getReceiver();
                row[3] = workRequest.getStatus();
                row[4] = workRequest.getResolveDate();
                Float cost = ((CarTestWorkRequest) workRequest).getCostOfRepair();
                row[5] = cost == null ? "Cost NA" : cost;
                model.addRow(row);
        }  

            WorkRequestjTable.setDefaultRenderer(Object.class, new TableCellRenderer(){
            private DefaultTableCellRenderer DEFAULT_RENDERER =  new DefaultTableCellRenderer();

            @Override
            public Component getTableCellRendererComponent(JTable WorkRequestjTable, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
                Component c = DEFAULT_RENDERER.getTableCellRendererComponent(WorkRequestjTable, value, isSelected, hasFocus, row, column);
                if(isSelected){
                    c.setBackground(Color.YELLOW);
                }else{
                if (row%2 == 0){
                    c.setBackground(Color.LIGHT_GRAY);

                }
                else {
                    c.setBackground(Color.WHITE);
                }
                }  

                       
                if(WorkRequestjTable.getColumnModel().getColumn(column).getIdentifier().equals("Status")){//Here `Status` is column name
                     if(value.toString().equals("Assigned")){//Here `OK` is the value of row
                         c.setBackground(Color.green);                        
                        }  
                }
                
                return c;
            }

        });        

    }
    
    
    
    
    
