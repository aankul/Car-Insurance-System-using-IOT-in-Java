     private void populateTable()
    {
        DefaultTableModel model = (DefaultTableModel) workRequestsJTable.getModel();

        model.setRowCount(0);
        for (WorkRequest workRequest : organization.getWorkQueue().getWorkRequestList()) {
                Object[] row = new Object[5];
                row[0] = workRequest;
                row[1] = workRequest.getRequestDate();
                row[2] = workRequest.getReceiver();
                row[3] = workRequest.getStatus();
                Float cost = ((CarTestWorkRequest) workRequest).getCostOfRepair();
                row[4] = cost == null ? "Cost NA" : cost;
                model.addRow(row);
        }  
        
             workRequestsJTable.setDefaultRenderer(Object.class, new TableCellRenderer(){
             private DefaultTableCellRenderer DEFAULT_RENDERER =  new DefaultTableCellRenderer();

            @Override
            public Component getTableCellRendererComponent(JTable workRequestsJTable, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
                Component c = DEFAULT_RENDERER.getTableCellRendererComponent(workRequestsJTable, value, isSelected, hasFocus, row, column);
                if(isSelected){
                    c.setBackground(Color.YELLOW);
                }else{
                if (row%2 == 0){
                    c.setBackground(Color.LIGHT_GRAY);

                }
                else {
                    c.setBackground(Color.WHITE);
                }
                }  

                       
                if(workRequestsJTable.getColumnModel().getColumn(column).getIdentifier().equals("Status")){//Here `Status` is column name
                     if(value.toString().equals("Sent to Insurance Company")){//Here `OK` is the value of row
                         c.setBackground(Color.green);                        
                        }  
                }
                
                return c;
            }

        });
        
        
        
        
        
    }
      
     
     private  void populateComboBox()
     {
        AssignRepairjComboBox.removeAllItems();
        
        for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList())
        {
            if(enterprise instanceof CarRepairEnterprise)
            {
                for(Organization organization : enterprise.getOrganizationDirectory().getOrganizationList())        
                    {
                        for (UserAccount userAccount : organization.getUserAccountDirectory().getUserAccountList())
                        {
                            AssignRepairjComboBox.addItem(userAccount);
                        }
                    }
            }
        }
     }
    
    
