    private void submitJPanelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJPanelActionPerformed
       
        int selectedRow = workRequestsJTable.getSelectedRow();
        WorkRequest selectedworkRequest;
        UserAccount selectedReceiver;
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Select a row", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            selectedworkRequest = (WorkRequest) workRequestsJTable.getValueAt(selectedRow, 0);
            selectedReceiver = (UserAccount) AssignRepairjComboBox.getSelectedItem();
            
            
            String status = selectedworkRequest.getStatus();            
            if(status.equals("Assigned") || status.equals("Car repairing Done"))
            {
                JOptionPane.showMessageDialog(this, "Request already assigned or repaired", "Information", JOptionPane.INFORMATION_MESSAGE);
                selectedworkRequest = null;
            }    
            else
            {    
            selectedworkRequest.setReceiver(selectedReceiver);
            selectedReceiver.getWorkQueue().getWorkRequestList().add(selectedworkRequest);
            JOptionPane.showMessageDialog(this, "Request Sent", "Information", JOptionPane.INFORMATION_MESSAGE);
            selectedworkRequest.setStatus("Assigned");
            populateTable();
            }
            
        }
        
// TODO add your handling code here:
    }//GEN-LAST:event_submitJPanelActionPerformed


