    private void populateTable()
    {
        DefaultTableModel model = (DefaultTableModel) WorkRequestjTable.getModel();
        model.setRowCount(0);
        for (WorkRequest workRequest : userAccount.getWorkQueue().getWorkRequestList()) {
                Object[] row = new Object[6];
                row[0] = workRequest;
                row[1] = workRequest.getRequestDate();
                row[2] = workRequest.getReceiver();
                row[3] = workRequest.getStatus();
                row[4] = workRequest.getResolveDate();
                Float cost = ((CarTestWorkRequest) workRequest).getCostOfRepair();
                row[5] = cost == null ? "Cost NA" : cost;
                model.addRow(row);
        }   
    }
    
    
    
    
    
